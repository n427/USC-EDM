// Helper function to get event by ID
export function getEventById(id: string) {
  const allTicketsAndEvents = [...myTickets, ...allEvents];
  // Remove duplicates (events that appear in both arrays)
  const uniqueEvents = allTicketsAndEvents.filter((event, index, self) =>
    index === self.findIndex((e) => e.id === event.id)
  );
  return uniqueEvents.find(event => event.id === id);
}